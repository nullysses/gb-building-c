uint8_t tiny_font_tile_for_char(uint8_t tile_base, char c) {
    const char *p = TINY_FONT_CHARS;
    uint8_t i = 0;

    while (*p) {
        if (*p == c) return tile_base + i;
        ++p;
        ++i;
    }

    return tile_base; /* space */
}
